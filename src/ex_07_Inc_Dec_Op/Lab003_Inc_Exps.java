package ex_07_Inc_Dec_Op;

public class Lab003_Inc_Exps {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a);

        int b = 10;
        System.out.println(b++ + ++b);
        System.out.println(b);

        int c = 10;
        System.out.println(++c + ++c);
        System.out.println(c);


    }
}
